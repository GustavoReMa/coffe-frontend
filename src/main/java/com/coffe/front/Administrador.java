    private int tableMousePressed(MouseEvent evt) {
        int row = 0;
        if (evt.getClickCount() == 2) {
            Point point = evt.getPoint(); //Obtiene los puntos de pantalla
            row = tabla.rowAtPoint(point); //Obtiene la coordenada en x de la tabla
        }
        return row;
    }

